#include "stdlib.h"
#include "libft.h"

void		ft_strtabfree(char ***tab)
{
	int		i;

	i = 0;
	while ((*tab)[i])
	{
		free((*tab)[i]);
		++i;
	}
	free(*tab);
	*tab = NULL;
}
